package video17;

public class switchtrial {
    public static void main(String[] args) {
        int x=4;
        switch (x) {
            case 2:
                System.out.println("hi");
                break;
            case 8:
                System.out.println("hii");
                break;
            default:
                System.out.println("hello"); }
    }
}
